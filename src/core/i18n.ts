// Translation hook
export function useTranslation(language: Language = 'en') {
  const t = (key: string, fallback?: string): string => {
    const translation = translations[language]?.[key] || translations.en[key] || fallback || key;
    return translation;
  };
  
  return { t, language };
}

// Get language direction for RTL languages
export function getLanguageDirection(language: Language): 'ltr' | 'rtl' {
  return language === 'ar' ? 'rtl' : 'ltr';
}

// Format currency based on language/region
export function formatCurrency(amount: number, language: Language = 'en'): string {
  const locale = {
    en: 'en-US',
    es: 'es-ES',
    fr: 'fr-FR',
    de: 'de-DE',
    pt: 'pt-BR',
    it: 'it-IT',
    ja: 'ja-JP',
    ko: 'ko-KR',
    zh: 'zh-CN',
    ar: 'ar-SA',
    ru: 'ru-RU',
    hi: 'hi-IN',
  }[language];
  
  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency: 'USD',
  }).format(amount);
}

// Format dates based on language/region
export function formatDate(date: Date | string, language: Language = 'en'): string {
  const locale = {
    en: 'en-US',
    es: 'es-ES',
    fr: 'fr-FR',
    de: 'de-DE',
    pt: 'pt-BR',
    it: 'it-IT',
    ja: 'ja-JP',
    ko: 'ko-KR',
    zh: 'zh-CN',
    ar: 'ar-SA',
    ru: 'ru-RU',
    hi: 'hi-IN',
  }[language];
  
  return new Intl.DateTimeFormat(locale).format(new Date(date));
}